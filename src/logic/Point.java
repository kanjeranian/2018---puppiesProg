package logic;

public class Point {
	private double x;
    private double y;
 
    //constructor, getters and setters
    public Point(double x,double y) {
    	this.x=x;
    	this.y=y;
    }

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
    
    

}
